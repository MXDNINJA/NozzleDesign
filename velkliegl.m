function [u,v,x,y] = velkliegl(z,r,rt,yt,g)

R = rt/yt;

%checked
u1 = r.^2/2 - 1/4 + z;
v1 = r.^3/4 - r/4 + r.*z;

% checked
u2 = (2*g + 9)/24.*r.^4 - ...
    (4*g + 15)/24.*r.^2 + ...
    (10*g + 57)/288 + ...
    z.*(r.^2 - 5/8) - ...
    (2*g - 3)/6.*z.^2;

% checked
v2 = (g + 3)/9.*r.^5 - ...
    (20*g + 63)/96.*r.^3 + ...
    (28*g + 93)/288.*r + ...
    z.*((2*g + 9)/6.*r.^3 - (4*g + 15)/12.*r) + ...
    r.*z.^2;

% checked
u3 = (556*g.^2 + 1737*g + 3069)/10368.*r.^6 - ...
    (388*g.^2 + 1161*g + 1881)/2304.*r.^4 + ...
    (304*g.^2 + 831*g + 1242)/1728.*r.^2 - ...
    (2708*g.^2 + 7839*g + 14211)/82944 + ...
    z.*((52*g.^2 + 51*g + 327)/384.*r.^4 - (52*g.^2 + 75*g + 279)/192.*r.^2 + (92*g.^2 + 180*g + 639)/1152) + ...
    z.^2.*(-(7*g - 3)/8.*r.^2 + (13*g - 27)/48) + ...
    z.^3.*(4*g.^2 + 57*g + 27)/144;

% checked
v3 = (6836*g.^2 + 23031*g + 30627)/82944.*r.^7 - ...
    (3380*g.^2 + 11391*g + 15291)/13824.*r.^5 + ...
    (3424*g.^2 + 11271*g + 15228)/13824.*r.^3 - ...
    (7100*g.^2 + 22311*g + 30249)/82944.*r + ...
    z.*((556*g.^2 + 1737*g + 3069)/1728.*r.^5 - (388*g.^2 + 1161*g + 1881)/576.*r.^3 + (304*g.^2 + 831*g + 1242)/864.*r) + ...
    z.^2.*((52*g.^2 + 51*g + 327)/192.*r.^3 - (52*g.^2 + 75*g + 279)/192.*r) - ...
    z.^3.*((7*g - 3)/12.*r);


u = 1 + u1./(R + 1) + (u1 + u2)./(R + 1).^2 + (u1 + 2*u2 + u3)./(R + 1).^3;
v = sqrt((g + 1)/2./(R + 1)).*(v1./(R + 1) + (3/2*v1 + v2)./(R + 1).^2 + (15/8*v1 + 5/2*v2 + v3)./(R + 1).^3);

y = r*yt;
x = z./sqrt(2*R./(g + 1)).*yt;

end